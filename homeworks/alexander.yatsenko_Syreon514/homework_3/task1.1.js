const firstVariable = 5;
const secondVariable = 15;
const thirdVariable = 53;

function square(number) {
    return number * number;
}

console.log(`${firstVariable} ^ 2 = ${square(firstVariable)}`);
console.log(`${secondVariable} ^ 2 = ${square(secondVariable)}`);
console.log(`${thirdVariable} ^ 2 = ${square(thirdVariable)}`);
